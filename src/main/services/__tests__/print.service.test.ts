// Minimal but realistic invoice/receipt fixtures, matching what
// billingService.getInvoice() actually returns via its Prisma query
// (src/main/services/billing.service.ts:339-358) — items carry BOTH the
// productName snapshot field (a plain scalar on InvoiceItem, always present)
// and a `product` relation object whose select clause only picks `id`/`unit`,
// never `productName`. A template that reads item.product.productName instead
// of item.productName renders a blank product-name cell on every invoice/
// receipt — this was a real, confirmed bug (found via an end-to-end print of
// an actual created invoice, not a unit test) fixed alongside Phase 39.
function makeInvoice() {
  return {
    invoiceNumber: 'INV-2026-000001',
    invoiceDate: '2026-07-03T00:00:00.000Z',
    status: 'ACTIVE',
    customer: { customerName: 'Print Verify Customer', phone: '9876500000', customerCode: 'CUS-00001' },
    items: [
      {
        productName: 'Print Verify Widget', // the real, always-populated snapshot field
        product: { unit: 'PCS' }, // deliberately NO productName here — matches getInvoice()'s actual select
        quantity: 2,
        unitPrice: 250,
        discountAmount: 0,
        taxRate: 18,
        taxAmount: 90,
        lineTotal: 500,
        variantInfo: null
      }
    ],
    subtotal: 500,
    discountAmount: 0,
    taxAmount: 90,
    roundingAmount: 0,
    totalAmount: 590,
    paidAmount: 590,
    balanceAmount: 0,
    paymentStatus: 'PAID',
    notes: null,
    gstType: 'CGST_SGST'
  }
}

const profile = {
  businessName: 'Test Business',
  currencySymbol: '₹',
  taxModel: 'GST'
}

describe('printService.generateInvoiceHtml', () => {
  it('renders the product name from the InvoiceItem snapshot field, not the (unselected) product relation', async () => {
    const html = await printService.generateInvoiceHtml(makeInvoice() as never, profile as never)
    expect(html).toContain('Print Verify Widget')
    expect(html).not.toContain('<td></td>') // the exact regression symptom: a blank product-name cell
  })

  it('still renders the unit from the product relation alongside the snapshot name', async () => {
    const html = await printService.generateInvoiceHtml(makeInvoice() as never, profile as never)
    expect(html).toContain('PCS')
  })
})

// Regression coverage for a real gap found and fixed 2026-07-08: the UPI QR
// only ever checked whether `upiId` was filled in, never the business's
// actual country — UPI is exclusively an Indian payment system, so a
// non-Indian business (or stale/garbage data left in that field) must never
// show a QR that makes no sense to its customers. Zero coverage existed for
// this feature before this pass.
describe('printService UPI QR country gating', () => {
  function makeUnpaidInvoice() {
    return { ...makeInvoice(), balanceAmount: 590, paymentStatus: 'PENDING' }
  }

  it('shows the UPI QR for an Indian business with a real upiId and an outstanding balance', async () => {
    const html = await printService.generateInvoiceHtml(makeUnpaidInvoice() as never, { ...profile, upiId: 'biz@upi', country: 'IN' } as never)
    expect(html).toContain('Scan to Pay (UPI)')
  })

  it('never shows the UPI QR for a non-Indian business, even with upiId filled in', async () => {
    const html = await printService.generateInvoiceHtml(makeUnpaidInvoice() as never, { ...profile, upiId: 'stale@upi', country: 'US' } as never)
    expect(html).not.toContain('Scan to Pay (UPI)')
  })

  // Regression for a real gap found 2026-07-13: SetupWizard.tsx's country
  // field is free text defaulting to "India" (never normalized to the ISO
  // code 'IN' anywhere), so a strict `country === 'IN'` check silently
  // disabled the QR for every real Indian setup that used the default value.
  it('shows the UPI QR for the real-world "India" free-text default, not just the ISO code', async () => {
    const html = await printService.generateInvoiceHtml(makeUnpaidInvoice() as never, { ...profile, upiId: 'biz@upi', country: 'India' } as never)
    expect(html).toContain('Scan to Pay (UPI)')
  })

  it('never shows the UPI QR when upiId is not configured, even for an Indian business', async () => {
    const html = await printService.generateInvoiceHtml(makeUnpaidInvoice() as never, { ...profile, country: 'IN' } as never)
    expect(html).not.toContain('Scan to Pay (UPI)')
  })

  it('never shows the UPI QR once the invoice is fully paid, regardless of country/upiId', async () => {
    const html = await printService.generateInvoiceHtml(makeInvoice() as never, { ...profile, upiId: 'biz@upi', country: 'IN' } as never)
    expect(html).not.toContain('Scan to Pay (UPI)')
  })
})

