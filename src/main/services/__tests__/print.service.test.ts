// Phase 44: logo + watermark support
const TEST_LOGO_PATH = join(tmpdir(), 'sarang-print-service-test-logo.png')
// Minimal valid 1x1 transparent PNG — enough for readFileSync + base64 to round-trip.
const ONE_PX_PNG_BASE64 = 'iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAQAAAC1HAwCAAAAC0lEQVR42mNkYAAAAAYAAjCB0C8AAAAASUVORK5CYII='
writeFileSync(TEST_LOGO_PATH, Buffer.from(ONE_PX_PNG_BASE64, 'base64'))
afterAll(() => { try { unlinkSync(TEST_LOGO_PATH) } catch { /* best-effort cleanup */ } })

// Module-scoped (not just describe-block-scoped) so every describe block below,
// including the Phase 45 Credit/Debit Note ones, can share these.
const profileWithWatermark = { ...profile, logoPath: TEST_LOGO_PATH, enableDocumentWatermark: true }
