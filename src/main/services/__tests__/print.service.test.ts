describe('logoToBase64DataUri', () => {
  it('returns a data: URI with the correct mime type and the file contents base64-encoded', () => {
    const uri = logoToBase64DataUri(TEST_LOGO_PATH)
    expect(uri).toBe(`data:image/png;base64,${ONE_PX_PNG_BASE64}`)
  })
})

